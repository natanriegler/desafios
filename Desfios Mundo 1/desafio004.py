algo = input('Digite algo: ')
print(type(algo))
print('É númerico?: ', algo.isnumeric())
print('É alfabetico?: ', algo.isalpha())
print('É alfanumerico?: ', algo.isalnum())
print('É Com letra(s) maísculas?: ', algo.isupper())
print('É com letra(s) minúsculas?:', algo.islower())
print('É com Espaços?: ', algo.isspace())
print('Está captalizada?: ', algo.istitle())



